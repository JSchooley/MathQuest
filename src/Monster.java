
public class Monster {
	
	private int damage;
	private int health;
	private int level;
	private int gold;
	private int experience;
	private int armor;
	
	public Monster(int damage, int health, int level, int gold, int experience, int armor) {
		this.damage = damage;
		this.health = health;
		this.level = level;
		this.gold = gold;
		this.experience = experience;
		this.armor = armor;
	}
}
